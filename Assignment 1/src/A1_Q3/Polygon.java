package A1_Q3;
/**
 * An interface for Polygons.
 * Contains methods for area and perimeter.
 * 
 * @author Andrew Greenwood
 */

public interface Polygon {
	
	/**
	 * @return the area of a polygon.
	 */
	public double area();
	
	/**
	 * @return the perimeter of a polygon.
	 */
	public double perimeter();
}
